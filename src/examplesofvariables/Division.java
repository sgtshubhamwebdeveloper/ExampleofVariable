package examplesofvariables;

public class Division {
	static void divisionnum(int x,int y)
	{
		int division = x/y;
		System.out.println("division of "+x+" and "+y+" is "+division);
	}
	public static void main(String args[])
	{
		int num1=56;
		int num2=7;
		divisionnum(num1,num2);
	}
	}


